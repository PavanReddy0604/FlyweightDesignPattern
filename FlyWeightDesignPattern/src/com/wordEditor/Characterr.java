package com.wordEditor;


public class Characterr {
	
	private char character;
	private String fontStyle;
	private int size;
	private int row;
	private int column;
	
	

	public Characterr() {
	}
	
	public Characterr(char character, String fontStyle, int size, int row, int column) {
		super();
		this.character = character;
		this.fontStyle = fontStyle;
		this.size = size;
		this.row = row;
		this.column = column;
	}
	

}
